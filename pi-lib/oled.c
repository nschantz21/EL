void OLEDDrawChar (int16_t x, int16_t y, char c, int16_t textColor, int16_t bgColor)
// Character draw function.
// Input: x         horizontal position of the top left corner of the character, columns from the left edge
//        y         vertical position of the top left corner of the character, rows from the top edge
//        c         character to be printed
//        textColor 16-bit color of the character
//        bgColor   16-bit color of the background
// Output: none
{
    uint8_t line;       // horizontal row of pixels of character
    int32_t col, row;   // loop indices
    uint16_t *local_fbp = (uint16_t *) fbp + x + y*x_res;
    
    if ((x >= OLED_WIDTH)  	|| // Clip right
        (y >= OLED_HEIGHT)	|| // Clip bottom
        (x < 0)        		|| // Clip left
        (y < 0))          		// Clip top
    return;
    
    line = 1;        // print the top row first
    
    // print the rows, starting at the top
    for (row = 0; row < OLED_CHAR_HEIGHT; row++)
    {
        // print the columns, starting on the left
        for (col = 0; col < OLED_CHAR_WIDTH; col++)
        {
            if (Font[(c*OLED_CHAR_WIDTH) + col] & line)
            {
                // bit is set in Font, print pixel(s) in text color
                *local_fbp++ = textColor;
            } else
            {
                // bit is cleared in Font, print pixel(s) in background color
            	*local_fbp++ = bgColor;
            }
        }
        // print blank column(s) to the right of character
        *local_fbp = bgColor;
        line = line << 1;   // move up to the next row
        local_fbp += x_res - OLED_CHAR_WIDTH;
    }
}

uint32_t OLEDDrawString (uint16_t row, uint16_t col, char *pt, int16_t textColor)
// String draw function.
// Input: row       rows from the top edge
//        col       columns from the left edge
//        pt        pointer to a null terminated string to be printed
//        textColor 16-bit color of the characters
// bgColor is Black 
// Output: number of characters printed
{
    uint32_t count = 0;
    
    if (row > OLED_ROWS) return 0;
    
    while (*pt)
    {
        OLEDDrawChar (col*(OLED_CHAR_WIDTH + 1), row*OLED_CHAR_HEIGHT, *pt++, textColor, ST7735_BLACK);
        col++;
        if (col > OLED_COLS) return count;  // number of characters printed
        count++;
    }
    return count;  // number of characters printed
}

void OLEDWriteNumber (int row, int col, int value, int len)
{
    char text[10];

    sprintf (text, "%*d", len, value);
    OLEDDrawString (row, col, text, ST7735_YELLOW);
}

void OLEDFillRect (int16_t x, int16_t y, int16_t w, int16_t h, uint16_t color)
// Draw a filled rectangle at the given coordinates with the given width, height, and color.
// Input: x     horizontal position of the top left corner of the rectangle, columns from the left edge
//        y     vertical position of the top left corner of the rectangle, rows from the top edge
//        w     horizontal width of the rectangle
//        h     vertical height of the rectangle
//        color 16-bit color
// Output: none
{
	uint16_t *local_fbp = (uint16_t *) fbp + x + y*x_res;
	int i, j;

	// rudimentary clipping (drawChar w/big text requires this)
    if ((x >= OLED_WIDTH) || (y >= OLED_HEIGHT)) return;
    if ((x + w - 1) >= OLED_WIDTH)  w = OLED_WIDTH  - x;
    if ((y + h - 1) >= OLED_HEIGHT) h = OLED_HEIGHT - y;
    
	for (i = 0; i < h; i++)
	{
		for (j = 0; j < w; j++)
			*local_fbp++ = color;
		local_fbp += x_res - w;
	}
}

void OLEDClearScreen (void)
{
	memset (fb_base, 0, finfo.smem_len);
}
