// Information about the display panel
struct fb_var_screeninfo vinfo;
struct fb_fix_screeninfo finfo;
/*
 * fb_base is set to the start of the frame buffer for the purpose of
 * clearing the entire screen. fbp is offset to account for the reduced
 * resolution of the panel.
 */
char *fbp, *fb_base;
int x_res;		// the size of a "line" in bytes

int OLEDInit (void)
{
    int fbfd, ret = -1;

    // Open the file for reading and writing
    fbfd = open ("/dev/fb1", O_RDWR);
    if (fbfd < 0)
    {
      printf ("Error: cannot open framebuffer device.\n");
      return ret;
    }
    // Get fixed screen information
    if (ioctl (fbfd, FBIOGET_FSCREENINFO, &finfo))
    {
        printf ("Error reading fixed information.\n");
        goto out;
    }
    if (ioctl (fbfd, FBIOGET_VSCREENINFO, &vinfo))
    {
    	printf ("Error reading variable information.\n");
        goto out;
    }
    // map framebuffer to user memory
    fb_base = (char*) mmap (0,
    					finfo.smem_len,
						PROT_READ | PROT_WRITE,
						MAP_SHARED,
						fbfd, 0);
    if (fb_base == (char *) -1)
    {
    	printf ("Failed to mmap.\n");
        goto out;
    }
#ifdef PORTRAIT
	fbp = fb_base + 0x30;			// 24 pixel "magic offset"
#else
	fbp = fb_base + 256*24 - 48;	// 24 line "magic offset"
#endif
/*
 * The panel has a resolution of 80 by 160. The memory in the ST7735S
 * controller chip is 128 pixels wide as reported in vinfo.xres. So
 * whenever we advance by a row we have to use the xres value rather
 * than the actual width of the screen.
 */
	x_res = vinfo.xres;
	OLEDClearScreen();
	ret = 0;

out:
	close (fbfd);
	return ret;
}

void OLEDDeInit (void)
{
	  munmap (fbp, finfo.smem_len);
}

